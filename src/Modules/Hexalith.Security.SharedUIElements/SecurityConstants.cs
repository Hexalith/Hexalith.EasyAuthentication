    /// </summary>
    public const string ClientPrincipalIdentityProviderHeader = "X-MS-CLIENT-PRINCIPAL-IDP";

    /// <summary>
    /// The header name for the client principal ID.
    /// </summary>
    public const string ClientPrincipalIdHeader = "X-MS-CLIENT-PRINCIPAL-ID";

