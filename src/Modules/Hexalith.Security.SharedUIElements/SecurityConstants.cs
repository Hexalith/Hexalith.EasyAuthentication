    /// </summary>
    public const string ClientPrincipalNameHeader = "X-MS-CLIENT-PRINCIPAL-NAME";

    /// <summary>
    /// The header name for the partition ID.
    /// </summary>
    public const string PartitionHeader = "HEXALITH-PARTITION-ID";
